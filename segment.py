class Segment:
    """
    Клас `Segment` використовується для представлення сегментів (відрізків) на координатній площині.

    Атрибути:
    start (tuple): Початкова точка відрізка у вигляді кортежу (x, y).
    end (tuple): Кінцева точка відрізка у вигляді кортежу (x, y).

    """
    def __init__(self, start, end):
        self.start = start
        self.end = end
